"""## 1 - Modelos HF Remotos"""

from langchain_huggingface import HuggingFaceEndpoint, ChatHuggingFace

"""Ver opções de modelos, provedores e preços em: https://huggingface.co/inference/models"""

model = HuggingFaceEndpoint(
    repo_id="moonshotai/Kimi-K2.6",
    provider="fireworks-ai",
    task="text-generation",
    max_new_tokens=1024,
)

remote_chat = ChatHuggingFace(llm=model)

"""Exemplo 1:"""

response1 = remote_chat.invoke(messages1)

